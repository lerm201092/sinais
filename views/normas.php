      <p>A continuación, están las recomendaciones para tener en cuenta durante la permanencia del paciente en la Clínica.</p>

        <h3>Elementos de uso personal</h3>
        <p>Al paciente hospitalizado se le debe proveer de los siguientes elementos de uso personal: pijamas, ropa interior, cepillo de dientes, crema dental, cepillo o peinilla para el cabello, pantuflas o sandalias, jabón de baño, toallas higiénicas y demás implementos de uso personal.</p>

        <h3>Dieta y nutrición</h3>
        <p>La alimentación del paciente está indicada por el médico tratante y/o nutricionista y cualquier incumplimiento a esta norma puede afectar el tratamiento apropiado.</p>

        <h3>Medicamentos</h3>
        <p>Todos los medicamentos deben ser ordenados por el médico y suministrados por el personal de enfermería. Si el paciente está tomando algún medicamento, debe informarle al médico tratante para que este, defina si se debe continuar administrando.</p>

        <p>Los medicamentos que no cubra el Plan Obligatorio de Salud (POS), deben ser cancelados por los familiares o acudientes del paciente, previa información que deberá suministrar el personal de salud.</p>


        <h3>Acompañantes</h3>
        <p>En caso que necesites un acompañante permanente, nuestra enfermera jefe dará la autorización para que estés acompañado por una persona de la familia o amigo(a) el cual debe ser una persona que te ofrezca compañía, confianza y seguridad.</p>

        <p>Durante la estadía en la Clínica el acompañante debe:</p>

        <ul>
            <li>Cumplir con las normas que la Clínica ha establecido</li>
            <li>La información referente a la salud del paciente será suministrada por el médico tratante.</li>
            <li>Informar al médico y al personal de enfermería sobre los cambios que observe en el estado de salud del paciente.</li>
            <li>Hablar con un volumen bajo y dirigirse al personal médico y de enfermería en forma respetuosa cuando solicite alguno de sus servicios.</li>
            <li>Estar en todo momento en la habitación del paciente e informar cuando deba dejar el paciente solo.</li>
            <li>No permitir hacer mal uso de los equipos que están en la clínica.</li>
            <li>No entrar por ningún motivo a otra habitación diferente de la asignada.</li>
            <li>Mantener las pertenencias del paciente en un lugar seguro.</li>
            <li>Antes de ingresar a las UCI´s o cualquier otra área es obligatorio el lavado de manos.</li>
